		<h2>HISTOIRE</h2>
		<p>
			Créé en 2017 par des vidéastes et technicien•ne•s du Pôle Audiovisuel du DOC, le FLiMM est non compétitif et à
			prix libre. Il met à l’honneur le moyen-métrage (25 à 65 minutes), un format qui a souvent du mal à trouver sa
			place dans les salles de cinéma et les festivals.
