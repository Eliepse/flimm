		<h2>DOC</h2>
		<p>
			DOC a été fondé par des artistes d’horizons et pratiques variés, en recherche d’espaces de travail. Située
			dans un ancien lycée technique, qu’elle squatte dans le XIXe arrondissement, l’association regroupe une centaine
			d’artistes et d’artisan•e•s. Depuis sa création en 2015, DOC a aidé à la production artistique, la diffusion et
			la transmission d'œuvres à travers de nombreux événements : expositions, concerts et spectacles.<br>
			<a href="https://doc.work" rel="noreferrer nofollow">doc.work</a>
		</p>

		—
		<h2>PENDANT LE FESTIVAL</h2>
		<p>
			Le DOC, ses deux salles de projections et le réfectoire, transformé en restaurant pour l’occasion, permettent
			des espaces de discussions entre les réalisateurs•trice•s et le public.
		</p>

		<h2>Restauration sur place</h2>
		<p>
			L’équipe de bénévoles du FLiMM vous propose ses créations culinaires, végétariennes et à prix libre.
		</p>

		<h2>Prix Libre</h2>
		<p>
			Tout le festival est à prix libre ! Le prix libre est une démarche sociale, culturelle et politique. Différent
			de la gratuité ou du prix fixe, le prix libre permet de rendre accessible le festival à tous•tes.<br>
			Vecteur de solidarité, le prix libre permet à chacun•e de donner selon ses moyens, les dons des plus aisé•e•s
			permettant l’accès aux salles et aux repas aux plus précaires.
		</p>

